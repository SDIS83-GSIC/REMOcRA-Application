interface IconProps {
  size?: string;
  className?: string;
}

function createIconComponent(
  SvgComponent: React.ComponentType<React.SVGProps<SVGSVGElement>>,
  style?: React.CSSProperties,
) {
  return function ({ size = "1em", className }: IconProps) {
    return (
      <span className={classNames(styles.icon, className)}>
        <SvgComponent style={{ height: size, width: size, ...style }} />
      </span>
    );
  };
}

